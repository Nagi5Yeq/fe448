void hex2bin(unsigned char *out, const char *in)
{
    char t;
    const char *p = in;
    unsigned char x;

    while (*p != '\0')
    {
        if (*p >= '0' && *p <= '9')
        {
            t = *p - '0';
        }
        else if (*p >= 'a' && *p <= 'z')
        {
            t = *p - 'a' + 10;
        }
        else
        {
            t = *p - 'A' + 10;
        }
        x = t << 4;
        p++;
        if (*p >= '0' && *p <= '9')
        {
            t = *p - '0';
        }
        else if (*p >= 'a' && *p <= 'z')
        {
            t = *p - 'a' + 10;
        }
        else
        {
            t = *p - 'A' + 10;
        }
        x += t;
        *out++ = x;
        p++;
    }
}

